<template>
    <div class="release-chart-burndown-container">
        <svg class="release-chart-burndown" v-bind:id="`chart-${release_data.id}`"></svg>
    </div>
</template>

<script lang="ts">
import { Component, Prop } from "vue-property-decorator";
import { MilestoneData } from "../../../../type";
import Vue from "vue";
import { createBurndownChart } from "../../../../../burndown_chart_builder/burndown-chart-drawer";
import { ChartPropsBurndownWhithoutTooltip } from "../../../../../../../../../src/www/scripts/charts-builders/type";

@Component
export default class BurndownChartDisplayer extends Vue {
    @Prop()
    readonly release_data!: MilestoneData;

    getChartProps(
        container_width: number,
        container_height: number
    ): ChartPropsBurndownWhithoutTooltip {
        return {
            graph_width: container_width,
            graph_height: container_height,
            margins: {
                top: 5,
                right: 20,
                bottom: 30,
                left: 35
            }
        };
    }

    mounted(): void {
        if (!this.release_data.burndown_data) {
            return;
        }

        const chart_container = document.getElementById("chart-" + this.release_data.id);

        if (chart_container) {
            createBurndownChart(
                chart_container,
                this.getChartProps(chart_container.clientWidth, chart_container.clientHeight),
                this.release_data.burndown_data
            );
        }
    }
}
</script>
